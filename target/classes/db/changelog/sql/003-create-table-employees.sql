CREATE TABLE IF NOT EXISTS employees
(
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login         VARCHAR(50) NOT NULL,
    password      VARCHAR(255) NOT NULL,
    age           INTEGER NOT NULL,
    employee_role VARCHAR(20) NOT NULL,
    created_at    TIMESTAMPTZ NOT NULL,
    updated_at    TIMESTAMPTZ NOT NULL,

    CONSTRAINT uk_employees_login UNIQUE (login),
    CONSTRAINT chk_employees_age CHECK (age >= 18)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_employees_login
    ON employees(login);

CREATE INDEX IF NOT EXISTS idx_employees_role
    ON employees(employee_role);